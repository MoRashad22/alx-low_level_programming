#include "3-calc.h"

/**
* op_add - adding two integers
* @a: 1st int
* @b: 2nd int
*
* Return: total
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtracting two integers
* @a: 1st int
* @b: 2nd int
*
* Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplying two ints
* @a: 1st int
* @b: 2nd int
*
* Return: multiplication result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - dividing two ints
* @a: 1st int
* @b: 2nd int
*
* Return: division product
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - modulo two int
* @a: 1st int
* @b: 2nd int
*
* Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
